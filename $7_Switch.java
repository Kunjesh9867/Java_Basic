//Switch statement in Java
public class $7_Switch {
    public static void main(String[] args) {
        int a = 5;
        switch (a) {
            case 1:
                System.out.println("Not Correct");
            case 2:
                System.out.println("Not Correct");
            case 3:
                System.out.println("Not Correct");
            case 4:
                System.out.println("Not Correct");
            case 5:
                System.out.println("Correct");
            default:
                System.out.println("Oops");
        }
    }
}
