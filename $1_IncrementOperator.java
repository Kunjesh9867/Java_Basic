// Understand ++ operator

public class $1_IncrementOperator {
    public static void main(String[] args) {
        int num1=17;
        int num2;
        num2=num1++; // Here, first num2=17 and then num1 will be incremented by 1 (num1=17) 
        System.out.print(num1);
        System.out.println(num2);
        System.out.println("Hello World");
    }
}

