//Understanding Modulo Operator
//make a program without using curly brackets {}

public class $4_ModuloOperator {
    public static void main(String[] args) {
        int num = 11;
        if(num%2==0) // No Curly brackets (Java will allow it, if only 1 statement is to be executed. If your want to perform more than 1 statement then you will have to use curly brackets)
            System.out.println("Number is Even");
        else // No Curly brackets
            System.out.println("Number is odd");
    }
}
